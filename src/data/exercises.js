export const EXERCISES_BY_ID = Object.fromEntries(EXERCISES.map((e) => [e.id, e]));

export function getExerciseById(id) {
  return EXERCISES_BY_ID[id];
}

export function getExercisesByCategory(category) {
  return EXERCISES.filter((e) => e.category === category);
}

export function getAlternativesFor(exercise) {
  if (!exercise) return [];
  const direct = (exercise.alternatives || [])
    .map((id) => EXERCISES_BY_ID[id])
    .filter(Boolean);
  const directIds = new Set(direct.map((e) => e.id));
  const sameCategory = EXERCISES.filter(
    (e) => e.category === exercise.category && e.id !== exercise.id && !directIds.has(e.id),
  );
  return [...direct, ...sameCategory];
}
